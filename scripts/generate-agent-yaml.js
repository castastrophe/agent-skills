import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const skillsDir = path.join(root, "skills");
const outputPath = path.join(root, "agent.yaml");

/**
 * Parse the YAML frontmatter from a SKILL.md file.
 * Returns an object with the frontmatter key/value pairs.
 */
function parseFrontmatter(content) {
	const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---/);
	if (!match) return {};

	return Object.fromEntries(
		match[1].split("\n").flatMap((line) => {
			const [key, ...rest] = line.split(":");
			if (!key || !rest.length) return [];
			return [[key.trim(), rest.join(":").trim().replace(/^["']|["']$/g, "")]];
		}),
	);
}

/**
 * Read all skill directories and return their names and metadata.
 */
function discoverSkills() {
	if (!fs.existsSync(skillsDir)) return [];

	return fs
		.readdirSync(skillsDir, { withFileTypes: true })
		.filter((entry) => entry.isDirectory())
		.map((entry) => {
			const skillMdPath = path.join(skillsDir, entry.name, "SKILL.md");
			const frontmatter = fs.existsSync(skillMdPath)
				? parseFrontmatter(fs.readFileSync(skillMdPath, "utf-8"))
				: {};

			// Prefer the name from frontmatter, fall back to the directory name
			return frontmatter.name || entry.name;
		})
		.sort();
}

/**
 * Build the YAML string from the provided data.
 * Written manually to preserve comment headers and key ordering without
 * requiring a YAML serialisation dependency.
 */
function buildYaml({ specVersion, name, version, description, author, license, model, skills, tags }) {
	const skillLines = skills.map((s) => `  - ${s}`).join("\n");
	const fallbackLines = model.fallback.map((m) => `    - ${m}`).join("\n");
	const tagLines = tags.map((t) => `  - ${t}`).join("\n");

	return [
		`# Auto-generated by scripts/generate-agent-yaml.js — do not edit by hand.`,
		`# Regenerate with: yarn generate-agent-yaml`,
		`spec_version: "${specVersion}"`,
		`name: ${name}`,
		`version: "${version}"`,
		`description: "${description}"`,
		`author: ${author}`,
		`license: ${license}`,
		`model:`,
		`  preferred: ${model.preferred}`,
		`  fallback:`,
		fallbackLines,
		`skills:`,
		skillLines,
		`tags:`,
		tagLines,
		"",
	].join("\n");
}

function main() {
	const pkg = JSON.parse(fs.readFileSync(path.join(root, "package.json"), "utf-8"));

	// Derive a clean package name (strip npm scope)
	const name = pkg.name.replace(/^@[^/]+\//, "");

	// Derive author handle from the author field (supports "Name <email> (url)" format)
	const authorStr = pkg.author || "";
	const authorMatch = authorStr.match(/\(([^)]+)\)/) || [];
	const authorHandle =
		(authorMatch[1] && authorMatch[1].split("/").pop()) ||
		authorStr.split(" ")[0] ||
		"unknown";

	const skills = discoverSkills();

	if (skills.length === 0) {
		console.warn("Warning: no skills found in skills/");
	}

	const yaml = buildYaml({
		specVersion: "0.1.0",
		name,
		version: pkg.version,
		description: pkg.description,
		author: authorHandle,
		license: pkg.license,
		model: {
			// TODO: Make this configurable or fetch from package.json?
			preferred: "claude-sonnet-4-6",
			fallback: ["claude-haiku-4-6"],
		},
		skills,
		tags: pkg.keywords || [],
	});

	fs.writeFileSync(outputPath, yaml, "utf-8");
	console.log(`agent.yaml written with ${skills.length} skill(s): ${skills.join(", ")}`);
}

main();
