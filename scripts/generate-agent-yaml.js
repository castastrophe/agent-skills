import fs from "fs";
import { readdir, readFile, writeFile } from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import { parseFrontmatter, getSkills } from "./parse-skills.js";
import "colors";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const skillsDir = path.join(root, "skills");
const outputPath = path.join(root, "agent.yaml");

/**
 * Build the YAML string from the provided data.
 * Written manually to preserve comment headers and key ordering without
 * requiring a YAML serialisation dependency.
 * @param {Object} data
 * @param {string} data.specVersion
 * @param {string} data.name
 * @param {string} data.version
 * @param {string} data.description
 * @param {string} data.author
 * @param {string} data.license
 * @param {Object} data.model
 * @param {string} data.model.preferred
 * @param {string[]} data.model.fallback
 * @param {string[]} data.skills
 * @param {string[]} data.tags
 * @returns {string}
 */
function buildYamlContent({
	specVersion,
	name,
	version,
	description,
	author,
	license,
	model,
	skills,
	tags,
}) {
	const skillLines = skills.map((s) => `  - ${s}`).join("\n");
	const fallbackLines = model.fallback.map((m) => `    - ${m}`).join("\n");
	const tagLines = tags.map((t) => `  - ${t}`).join("\n");

	return [
		`# Auto-generated by scripts/generate-agent-yaml.js — do not edit by hand.`,
		`# Regenerate with: yarn generate:agent-yaml`,
		`spec_version: "${specVersion}"`,
		`name: ${name}`,
		`version: "${version}"`,
		`description: "${description}"`,
		`author: ${author}`,
		`license: ${license}`,
		`model:`,
		`  preferred: ${model.preferred}`,
		`  fallback:`,
		fallbackLines,
		`skills:`,
		skillLines,
		`tags:`,
		tagLines,
		"",
	].join("\n");
}

async function main() {
	const pkg = await readFile(path.join(root, "package.json"), "utf-8").then((content) => JSON.parse(content));
	if (!pkg) {
		return Promise.reject(new Error(`Failed to read package.json in ${path.relative(root, "package.json").cyan}`));
	}

	const skills = await getSkills(
		async (entry) => {
			const skillMdPath = path.join(skillsDir, entry.name, "SKILL.md");
			if (fs.existsSync(skillMdPath)) {
				const content = await readFile(skillMdPath, "utf-8").then((content) => parseFrontmatter(content));

				// If the SKILL.md file exists, use the name and description from the frontmatter
				return Promise.resolve({
					name: content.name ?? entry.name,
					description: content.description ?? "",
					path: entry.name,
				});
			}

			// If the SKILL.md file doesn't exist, use the directory name as the name and description
			return Promise.resolve({
				name: entry.name,
				description: "",
				path: entry.name,
			});
		}
	).then((skills) => skills.sort((a, b) => a.name.localeCompare(b.name))).catch((err) => Promise.reject(new Error(`Failed to get skills: ${err.message}`)));

	const content = buildYamlContent({
		specVersion: "0.1.0",
		name: pkg.name.replace(/^@[^/]+\//, ""),
		version: pkg.version ?? "0.0.0",
		description: pkg.description ?? "",
		author: pkg.author?.replace(/^@[^/]+\//, "") ?? "",
		license: pkg.license ?? "Apache-2.0",
		model: {
			preferred: pkg.model?.preferred ?? "claude-sonnet-4-6",
			fallback: pkg.model?.fallback ?? ["claude-haiku-4-6"],
		},
		skills,
		tags: pkg.keywords ?? [],
	});

	return writeFile(outputPath, content, "utf-8").then(() => Promise.resolve(skills)).catch((err) => Promise.reject(new Error(`Failed to write ${'agent.yaml'.cyan} in ${path.relative(root, outputPath).cyan}: ${err.message}`)));
}

main().then((skills) => {
	console.log(`${"✓".green} ${'agent.yaml'.cyan} generated with: ${skills.map((s) => s.name.magenta).join(", ")}`);
	process.exit(0);
}).catch((err) => {
	console.error(`${"✗".red} ${err.message}`);
	process.exit(1);
});
